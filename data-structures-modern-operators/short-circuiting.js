// Operador OR Retorna o primeiro valor verdadeiro encontrado
// Ou retornar o último valor, caso todos eles sejam valores false
console.log(3 || "Maria");
console.log("" || "Maria");
console.log(true || 0);
console.log(undefined || null);

// Operador AND Retorna o primeiro valor falso encontrado caso exista
// Caso não exista retorna o último falor true encontrado
console.log(0 && "Maria");
console.log(7 && "Maria");
