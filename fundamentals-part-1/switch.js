const day = 2;

switch(day) { // Valor a ser comparado
    case 1: console.log("É Segunda-feira"); break;
    case 2: console.log("É Terça-feira"); break; // Break é responsável por finalizar o código assim que ele encontrar o case correspondente.
    default: console.log("Não identificamos o dia"); // É chamado quando nenhum dos case corresponde ao valor.
}
